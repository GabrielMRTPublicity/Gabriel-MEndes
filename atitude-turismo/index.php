    <script>
    $(document).ready(function() {
        const imageWidthWithMargin = 335 + 10; // Largura da imagem + margem direita
        const totalImages = $('#carousel-1 img').length;
        const animationSpeed = 0.5; // Velocidade de animação em pixels por frame

        // Clone the first and last 4 images for the continuous effect
        function cloneImages(carouselId) {
            const $carousel = $(carouselId);
            const $firstImages = $carousel.find('img').slice(0, 4).clone();
            const $lastImages = $carousel.find('img').slice(-4).clone();
            $carousel.find('div').append($firstImages);
            $carousel.find('div').prepend($lastImages);
        }

        cloneImages('#carousel-1');
        cloneImages('#carousel-2');

        let position1 = -4 * imageWidthWithMargin; // Start position after the cloned images
        let position2 = -totalImages * imageWidthWithMargin; // Start position before the cloned images

        function animateCarousel(carouselId, position, direction) {
            const $carousel = $(carouselId).find('div');
            position += direction === 'forward' ? -animationSpeed : animationSpeed;
            $carousel.css('transform', 'translateX(' + position + 'px)');

            if (direction === 'forward' && position <= -(totalImages + 4) * imageWidthWithMargin) {
                position = -4 * imageWidthWithMargin;
                $carousel.css('transition', 'none');
                $carousel.css('transform', 'translateX(' + position + 'px)');
                setTimeout(() => $carousel.css('transition', 'transform 0.1s linear'), 20);
            } else if (direction === 'backward' && position >= -4 * imageWidthWithMargin) {
                position = -totalImages * imageWidthWithMargin;
                $carousel.css('transition', 'none');
                $carousel.css('transform', 'translateX(' + position + 'px)');
                setTimeout(() => $carousel.css('transition', 'transform 0.1s linear'), 20);
            }

            requestAnimationFrame(() => animateCarousel(carouselId, position, direction));
        }

        requestAnimationFrame(() => animateCarousel('#carousel-1', position1, 'forward'));
        requestAnimationFrame(() => animateCarousel('#carousel-2', position2, 'backward'));
    });
    </script>
